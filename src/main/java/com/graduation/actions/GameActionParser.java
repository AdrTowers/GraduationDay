package com.graduation.actions;

class GameActionParser {
    private String go;
    private String get;
    private String validDirection;
    private String item;
    private String invalidMove;
    private String enterMove;
    private String north;
    private String south;
    private String east;
    private String west;
    private String itemLevel;

    public String getGo() {
        return go;
    }

    public String getGet() {
        return get;
    }

    public String getValidDirection() {
        return validDirection;
    }

    public String getItem() {
        return item;
    }

    public String getInvalidMove() {
        return invalidMove;
    }

    public String getEnterMove() {
        return enterMove;
    }

    public String getNorth() {
        return north;
    }

    public String getSouth() {
        return south;
    }

    public String getEast() {
        return east;
    }

    public String getWest() {
        return west;
    }

    public String getItemLevel() {
        return itemLevel;
    }
}